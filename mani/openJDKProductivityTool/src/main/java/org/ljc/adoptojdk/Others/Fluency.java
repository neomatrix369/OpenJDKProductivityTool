package org.ljc.adoptojdk.Others;

public class Fluency {

	public static boolean is(boolean expression) {
		return expression;
	}

	public static boolean ifItIs(boolean expression) {
		return expression;
	}

	public static boolean ifThe(boolean expression) {
		return expression;
	}
	
	public static boolean not(boolean equals) {
		return !equals;
	}	

}